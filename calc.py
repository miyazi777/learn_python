# 四則演算
x = 16
y = 3
val = x + y
print(val)  # 19
val = x - y
print(val)  # 13
val = x * y
print(val)  # 48
val = x / y
print(val)  # 5.3333
val = x // y
print(val)  # 5
val = x % y
print(val)  # 1
val = int(x / y)
print(val)  # 5
val = float(x)
print(val)  # 16.0

# 複素数
val = 3 + 5j
print(val.conjugate())  # (3-5j)

# 乗数
val = pow(2, 3)
print(val)  # 8
val = 2 ** 3
print(val)  # 8

