# int
num = 5
print(num)  # 5
bin_val = 0b0101
print(bin_val)  # 5
oct_val = 0o165
print(oct_val)  # 117
hex_val = 0xfaa
print(hex_val)  # 4010

# float
x = 0.0012
print(x)    # 0.0012
y = 1.2e-3
print(y)    # 0.0012

# complex
c1 = 2 + 3j
print(c1)   # (2+3j)
c2 = complex(5, 6)
print(c2)   # (5+6j)

print(c1.real)  # 2.0
print(c2.imag)  # 6.0
