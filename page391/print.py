print('文字列') # 文字列

x = 'text'
print(x)    # text

y = 100
print(y)    # 100

print(x, y) # text 100

# 改行させない
print('aaa', end='')    # aaa
print('bbb', end='')    # aaabbb

# 文字列結合
x1 = 'text'
x2 = 'moji'
print(x1 + ' ' + x2)    # text moji

# 数値を文字列に変換
x = 'text'
y = 100
print(x + str(y))   # text100
